export async function updateTask(
  id: string,
  updates: Partial<
    Pick<
      ScheduledTask,
      'prompt' | 'schedule_type' | 'schedule_value' | 'next_run' | 'status'
    >
  >,
): Promise<void> {
  if (Object.keys(updates).length === 0) return;
  const { error } = await supabase.from('agents').update(updates).eq('id', id);
  if (error) logger.error({ error, taskId: id }, 'Failed to update task');
}

export async function deleteTask(id: string): Promise<void> {
  // agent_runs cascade-deleted via FK ON DELETE CASCADE
  const { error } = await supabase.from('agents').delete().eq('id', id);
  if (error) logger.error({ error, taskId: id }, 'Failed to delete task');
}

export async function getDueTasks(): Promise<ScheduledTask[]> {
  const now = new Date().toISOString();
  const { data, error } = await supabase
    .from('agents')
    .select()
    .eq('status', 'active')
    .not('next_run', 'is', null)
    .lte('next_run', now)
    .order('next_run');
  if (error) {
    logger.error({ error }, 'Failed to get due tasks');
    return [];
  }
  return (data ?? []) as ScheduledTask[];
}

export async function updateTaskAfterRun(
  id: string,
  nextRun: string | null,
  lastResult: string,
): Promise<void> {
  const now = new Date().toISOString();
  const updates: Record<string, unknown> = {
    next_run: nextRun,
    last_run: now,
    last_result: lastResult,
  };
  if (nextRun === null) {
    updates.status = 'completed';
  }
  const { error } = await supabase.from('agents').update(updates).eq('id', id);
  if (error)
    logger.error({ error, taskId: id }, 'Failed to update task after run');
}

